create table if not exists public.pemilik (
  id_pemilik varchar(10) primary key,
  nama_pemilik varchar(50),
  telepon varchar(15)
);

create table if not exists public.kamar (
  id_kamar varchar(10) primary key,
  id_pemilik varchar(10) references public.pemilik(id_pemilik) on update cascade,
  nomor_kamar varchar(10),
  harga_sewa integer default 0,
  status varchar(20) default 'Kosong' check (status in ('Kosong', 'Terisi'))
);

create table if not exists public.penyewa (
  id_penyewa varchar(10) primary key,
  id_kamar varchar(10) references public.kamar(id_kamar) on update cascade on delete set null,
  nama_penyewa varchar(50),
  no_hp varchar(15),
  tanggal_masuk date
);

create table if not exists public.pembayaran (
  id_pembayaran varchar(10) primary key,
  id_penyewa varchar(10) references public.penyewa(id_penyewa) on update cascade on delete cascade,
  jumlah_bayar integer default 0,
  tanggal_bayar date,
  status_bayar varchar(20) default 'Belum Bayar' check (status_bayar in ('Lunas', 'Belum Bayar'))
);

create table if not exists public.log_pembayaran (
  id_log bigint generated by default as identity primary key,
  id_pembayaran varchar(10),
  aksi varchar(20),
  waktu timestamptz default now()
);

create table if not exists public.akun (
  id uuid primary key references auth.users(id) on delete cascade,
  email text unique not null,
  nama text,
  role text not null default 'admin' check (role in ('admin', 'owner')),
  dibuat_pada timestamptz not null default now(),
  diperbarui_pada timestamptz not null default now()
);

create or replace function public.sync_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.diperbarui_pada = now();
  return new;
end;
$$;

drop trigger if exists before_akun_update on public.akun;
create trigger before_akun_update
before update on public.akun
for each row execute function public.sync_updated_at();

create or replace function public.create_akun_for_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.akun (id, email, nama)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'nama', split_part(new.email, '@', 1))
  )
  on conflict (id) do update
  set email = excluded.email,
      nama = coalesce(public.akun.nama, excluded.nama);

  return new;
end;
$$;

drop trigger if exists after_auth_user_created on auth.users;
create trigger after_auth_user_created
after insert on auth.users
for each row execute function public.create_akun_for_auth_user();

insert into public.akun (id, email, nama)
select id, email, coalesce(raw_user_meta_data ->> 'nama', split_part(email, '@', 1))
from auth.users
where email is not null
on conflict (id) do update
set email = excluded.email,
    nama = coalesce(public.akun.nama, excluded.nama);

create or replace function public.sync_kamar_status()
returns trigger
language plpgsql
as $$
begin
  update public.kamar
  set status = case
    when exists (
      select 1 from public.penyewa
      where penyewa.id_kamar = kamar.id_kamar
    )
    then 'Terisi'
    else 'Kosong'
  end;

  return null;
end;
$$;

drop trigger if exists after_penyewa_change on public.penyewa;
create trigger after_penyewa_change
after insert or update or delete on public.penyewa
for each statement execute function public.sync_kamar_status();

create or replace function public.log_after_insert_pembayaran()
returns trigger
language plpgsql
as $$
begin
  insert into public.log_pembayaran (id_pembayaran, aksi, waktu)
  values (new.id_pembayaran, 'Pembayaran Masuk', now());

  return new;
end;
$$;

drop trigger if exists after_insert_pembayaran on public.pembayaran;
create trigger after_insert_pembayaran
after insert on public.pembayaran
for each row execute function public.log_after_insert_pembayaran();

create or replace function public.cek_status_penyewa(p_id_penyewa varchar)
returns varchar
language sql
stable
as $$
  select case
    when exists (
      select 1
      from public.pembayaran
      where id_penyewa = p_id_penyewa
        and status_bayar = 'Belum Bayar'
    )
    then 'Nunggak'
    else 'Lunas'
  end;
$$;

create or replace function public.total_pendapatan(p_bulan integer, p_tahun integer)
returns integer
language sql
stable
as $$
  select coalesce(sum(jumlah_bayar), 0)::integer
  from public.pembayaran
  where extract(month from tanggal_bayar)::integer = p_bulan
    and extract(year from tanggal_bayar)::integer = p_tahun
    and status_bayar = 'Lunas';
$$;

